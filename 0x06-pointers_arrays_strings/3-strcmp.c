#include "main.h"
#include <string.h>
/**
 * strcmp - compares two strings
 * 
 * @s1: first string
 * @s2: second string
 *
 * Return:
 * 	returns zero if s1 == s2
 * 	returns negative number if s1 < s2
 * 	returns positive number if s1 > s2
 */
int _strcmp(char *s1, char *s2)
{
	return strcmp(s1, s2);
}
